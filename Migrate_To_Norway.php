                                                <p>Broadly speaking, the award of a residence permit depends on two
                                                    things: your country of citizenship and your ability to financially
                                                    sustain yourself and any dependents. This generally means you need a
                                                    job offer, or you must prove financial sustainability if you are
                                                    coming to Norway to study or for some other reason.

                                                    It is possible for some people to move to Norway as a job seeker,
                                                    but this is only allowed for a relatively short period of time. If
                                                    you don't manage to find employment during that time, you will have
                                                    to leave the country, and there is a waiting period before you will
                                                    be able to return.

                                                    The Department of Immigration (UDI) and the Police are the two
                                                    authorities responsible for managing the immigration process. While
                                                    applications take place in person, the process of booking
                                                    appointments is done online via UDI.</p>

                                                <p>If you've never even visited Norway before, the best thing you can do
                                                    is to plan a trip. You could of course tour the fjords at the height
                                                    of summer, but that will only give you a false impression of
                                                    Norwegian lifestyle.

                                                    Instead, spend some time in your chosen city during the darkness and
                                                    low temperatures of the winter. You'll be living like this for
                                                    months every year, so it's a great idea to give this time of the
                                                    year a “test run” before you decide to move.</p>

                                                <br> <h4 style="text-align: left" class="custom_heading"><strong>Cost of
                                                        living</strong></h4><br>

                                                <div class="wpb_text_column wpb_content_element custom_1570255449293">
                                                    <p class="wpb_wrapper">
                                                    </p><p>Although Norway has some of the highest salaries in the
                                                        world, they do come with a vastly increased cost of living.
                                                        Because of those same high wages, goods and services are
                                                        more expensive than in almost any other country.

                                                        It's essential to understand the cost of living in your
                                                        chosen part of the country, and the relationship between
                                                        your expected salary and everyday expenses.

                                                        If after thorough research you're still keen on a move to
                                                        Norway, then the next steps depend on your country of
                                                        citizenship.

                                                        Broadly speaking, the rules are split into three overarching
                                                        categories, with an increasing level of difficulty as you
                                                        move down the list:

                                                    </p><ul>
                                                        <li>Nordic citizens</li>
                                                        <li> EU/EEA citizens</li>
                                                        <li> The rest of the world
